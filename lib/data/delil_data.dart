List<Delil> getRecommended() =>
    recommendedIds.map((id) => allDeliller.firstWhere((d) => d.id == id)).toList();

List<Delil> getByCategory(String category) =>
    allDeliller.where((d) => d.category == category).toList();

List<String> get allCategories => [
  _felsefe, _bilim, _risale, _fitrat, _itiraz, _kainat,
];

List<Delil> search(String query) {
  final q = query.toLowerCase();
  return allDeliller.where((d) =>
    d.title.toLowerCase().contains(q) ||
    d.short.toLowerCase().contains(q) ||
    d.expanded.toLowerCase().contains(q) ||
    d.tags.any((t) => t.toLowerCase().contains(q))
  ).toList();
}
